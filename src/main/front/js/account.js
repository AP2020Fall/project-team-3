function account(player, firstName, lastName, username, phone, email, year, month, day, level, money) {
    this.player = player;
    this.firstName = firstName;
    this.lastName = lastName;
    this.username = username;
    this.phone = phone;
    this.email = email;
    this.year = year;
    this.month = month;
    this.day = day;
    this.level = level;
    this.money = money;
}